"""
Tipos de dados
str - string - textos 'Assim' "Assim"
int - inteiro - 123456 0 -20 -50 -60
float - real/ponto flutuante - 10.50 1.5 -10.10 -50.93
bool - booleano/lógico - True/False 10 == 10
"""
print('luiz', type('Luiz'))
print(10, type(10))
print(25.23, type(25.23))
print('L' == 'L', type('L' == 'L'))
print('')
# String: nome
print('Adriano Santos', type('Adriano Santos'))
# Idade: int
print(45, type(45))
# Altura: float
print(1.85, type(1.85))
# É maior de idade 10 > 20
print(45 > 18, type(45 > 18))
